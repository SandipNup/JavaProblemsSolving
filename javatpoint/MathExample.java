public class MathExample {
    public static void main(String[] args) {
        double x = 28;
        double y = 4;

        System.out.println("Max value is "+ Math.max(x, y));
        System.out.println("Square root is " + Math.sqrt(4));
        System.out.println("28 powers 4 is" + Math.pow(x, y));


        // logarithm
        System.out.println("Logarithm of x is "+ Math.log(x));
        System.out.println("Logarithm of y is "+ Math.log(y));


        // return log (x + 1)
        System.out.println("log 1 p of x is " + Math.log1p(x));

        // returns power of 2
    }
}
